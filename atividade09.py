# Atividade 09 – Cálculo de Área
# Peça ao usuário a largura e a altura de um retângulo.
# Calcule e mostre a área total

largura = int(input("digite a largura do retângulo: "))
altura = int(input("digite a altura do retângulo: "))
area_total = largura * altura
print(f'a área total desse retângulo é: {area_total}. ')
